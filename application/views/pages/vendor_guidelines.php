    <section class="dl-page-section" id="listings">
        <h2>Product Listings</h2>
        <ul class="dl-checklist">
            <li>Titles and descriptions must be accurate and written in English.</li>
            <li>Photos must show the actual item — misleading stock images are not allowed.</li>
            <li>Prices must be in SGD and inclusive of all applicable taxes.</li>
            <li>Products must comply with Singapore consumer safety laws, especially for children's items.</li>
            <li>Counterfeit, recalled, or prohibited goods are strictly forbidden.</li>
        </ul>
    </section>

    <section class="dl-page-section" id="fulfillment">
        <h2>Order Fulfillment</h2>
        <ul class="dl-checklist">
            <li>Process orders within <strong>3 business days</strong> of payment confirmation.</li>
            <li>Update order status to Shipped promptly with accurate dispatch details.</li>
            <li>Notify buyers proactively if there are any delays before they ask.</li>
        </ul>
    </section>

    <section class="dl-page-section" id="service">
        <h2>Customer Service</h2>
        <p>Sellers are responsible for resolving buyer disputes within <strong>5 business days</strong>. Unresolved disputes escalate to DLIdeas admin and can result in account suspension. Prompt, professional communication is expected at all times.</p>
    </section>

    <section class="dl-page-section" id="prohibited">
        <h2>Prohibited Items</h2>
        <ul class="dl-checklist dl-checklist--no">
            <li>Weapons, dangerous chemicals, or hazardous materials</li>
            <li>Adult content or age-restricted products</li>
            <li>Counterfeit or unlicensed branded goods</li>
            <li>Any item violating Singapore law or our Terms of Service</li>
        </ul>
        <p style="margin-top: var(--space-3);">Listings that violate these rules are removed immediately and repeated violations result in permanent suspension.</p>
    </section>

